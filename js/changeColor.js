function changeColor(string) {
    if (string === 'like') {
        document.getElementById("like").style.color = "blue";
        document.getElementById("dis").style.color = "#7d7d7d";
    } 
    else if (string === 'dislike') {
        document.getElementById("dis").style.color = "blue";
        document.getElementById("like").style.color = "#7d7d7d";
    }

}
